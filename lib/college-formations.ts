export type FixedFormationSubjectDef = {
  branch_name: string;
  branch_type: "DEPARTMENT" | "BRANCH";
};

/** تعريفات القسم/الفرع الثابتة لكل تشكيل يُعرض فيه حقل اختيار معتمد (مع نوع السجل). */
