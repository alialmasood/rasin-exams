/** قائمة أسماء القسم/الفرع الثابتة فقط (للتحقق من الصلاحية وما شابه). */
export function getFixedCollegeDepartmentNamesForFormation(formationTrimmed: string): readonly string[] | null {
  const defs = getFixedFormationSubjectDefinitions(formationTrimmed);
  return defs ? defs.map((d) => d.branch_name) : null;
}

